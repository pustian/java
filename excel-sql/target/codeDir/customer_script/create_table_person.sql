CREATE TABLE person (
  id int NOT NULL AUTO_INCREMENT, 
  customer_id char (24) NOT NULL, 
  phone_no char (11), 
  wechat_no varchar (24), 
  email varchar (60), 
  birthday char (8), 
  sex char (1), 
  marital_status char (1), 
  degree char (1), 
  birth_province char (2), 
  birth_city char (2), 
  birth_district char (2), 
  birth_street varchar (120), 
  residence_province char (2), 
  residence_city char (2), 
  residence_district char (2), 
  residence_street varchar (120), 
  residence_type char (1), 
  residence_years tinyint (1), 
  work_years tinyint (1), 
  security_no varchar (15), 
  monthly_incoming int (4), 
  idcard_image_url varchar (40), 
  idcard_image1 varchar (200), 
  idcard_image2 varchar (200), 
  employee_card_image varchar (200), 
  business_card_image varchar (200), 
  company_name varchar (180), 
  company_province char (2), 
  company_city char (2), 
  company_district char (2), 
  company_street varchar (120), 
  company_character char (1), 
  company_industry char (1), 
  company_phone varchar (20), 
  department_job varchar (20), 
  headship varchar (20), 
  work_hours tinyint (1), 
  create_time DateTime, 
  modified_time DateTime, 
  PRIMARY KEY (id)
) ENGINE=INNODB DEFAULT CHARSET=UTF8;